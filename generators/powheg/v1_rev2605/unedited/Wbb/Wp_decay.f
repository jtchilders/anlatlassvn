      subroutine setborn_Wpdecay(p,bflav,born)
      implicit none
      include 'nlegborn.h'
      include 'pwhg_flst.h'
      include 'pwhg_math.h'
      include 'pwhg_st.h'
      include 'PhysPars.h'
      include 'coupl.inc'       ! Madgraph common block
      real * 8 p(0:3,nlegborn+1)
      integer bflav(nlegborn+1),mu,i
      real * 8 born
      real * 8 gs2,gweak      
      logical ini
      data ini/.true./
      save ini
c
      if (ini) then
c     initialize MadGraph couplings 
         call initialize
         ini=.false.
      endif      
      gs2 = 4*pi*st_alpha
      gweak = ph_unit_e/ph_sthw
c     Set MADGRAPH Wmass to the pole W mass
      WMASS=ph_Wmass
      if (bflav(1).eq.2.or.bflav(1).eq.4) then
         call SUDB_EPVEBBB(p,born)
      elseif (bflav(2).eq.2.or.bflav(2).eq.4) then
         call SDBU_EPVEBBB(p,born)
      else
         write(*,*) 'Error in W+ decay. No Born diagram'
         call exit(1)
      endif
c     add CKW 
      born = born*ph_CKM_matrix(abs(bflav(1)),abs(bflav(2)))**2
      born = born* (gweak/(gwf(1)*dsqrt(2d0)))**4 * gs2**2
      end

      subroutine setreal_Wpdecay(p,rflav,amp2)
      implicit none
      include 'nlegborn.h'
      include 'pwhg_flst.h'
      include 'pwhg_math.h'
      include 'pwhg_st.h'
      include 'PhysPars.h'
      include 'coupl.inc'       ! Madgraph common block
      real * 8 p(0:3,nlegreal+1)
      integer rflav(nlegreal+1) 
      real * 8 amp2
      real * 8 gs2,gweak
      logical ini
      data ini/.true./
      save ini
c
      if (ini) then
c     initialize MadGraph couplings
         call initialize
         ini=.false.
      endif
      gs2 = 4*pi*st_alpha
      gweak = ph_unit_e/ph_sthw
c     Set MADGRAPH Wmass to the pole W mass
      WMASS=ph_Wmass

      if (rflav(1).ne.0.and.rflav(2).ne.0) then
         if (rflav(1).eq.2.or.rflav(1).eq.4) then
            call SUDB_EPVEBBBG(p,amp2)
         else
            call SDBU_EPVEBBBG(p,amp2)
         endif
         amp2=amp2*ph_CKM_matrix(abs(rflav(1)),abs(rflav(2)))**2
      elseif (
c     gu
     #        (rflav(1).eq.0.and.rflav(2).eq.2).or.
c     gc
     #        (rflav(1).eq.0.and.rflav(2).eq.4)) then
         call SGU_EPVEBBBD(p,amp2)
         amp2=amp2*ph_CKM_matrix(abs(rflav(2)),abs(rflav(7)))**2
      elseif (
c     ug
     #        (rflav(1).eq.2.and.rflav(2).eq.0).or.
c     cg
     #        (rflav(1).eq.4.and.rflav(2).eq.0)) then
         call SUG_EPVEBBBD(p,amp2)
         amp2=amp2*ph_CKM_matrix(abs(rflav(1)),abs(rflav(7)))**2
      elseif (
c     gdb
     #        (rflav(1).eq.0.and.rflav(2).eq.-1).or.
c     gsb
     #        (rflav(1).eq.0.and.rflav(2).eq.-3)) then
         call SGDB_EPVEBBBUB(p,amp2)
         amp2=amp2*ph_CKM_matrix(abs(rflav(2)),abs(rflav(7)))**2
      elseif (
c     dbg
     #        (rflav(1).eq.-1.and.rflav(2).eq.0).or.
c     sbg
     #        (rflav(1).eq.-3.and.rflav(2).eq.0)) then
         call SDBG_EPVEBBBUB(p,amp2)
         amp2=amp2*ph_CKM_matrix(abs(rflav(1)),abs(rflav(7)))**2
      else
         write(*,*) 'wrong calling sequence in setreal'
         write(*,*) 'rflav =',rflav
         call exit(1)
      endif
c     We should put back the factor gs^6=gs2^3
c     amp2 = amp2 * gs2^3 
c     STRIP OFF a factor of as/(2pi) = gs2/(4*pi)/(2*pi)=gs2/(8*pi^2)
C     ==> amp2=amp2 * gs2^2 * (8*pi^2)
      amp2=amp2*gs2**2*(gweak/(gwf(1)*dsqrt(2d0)))**4*8*pi**2
      end

